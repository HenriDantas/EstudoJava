package Exercicios.pt4_guardarDados.ex4;

public class Veiculo {
    private String nome;
    private int ano;
    private String cor;
    private String placa;
    private String dono;

    public Veiculo(String nome, int ano, String cor, String placa, String dono){
        this.nome = nome;
        this.ano = ano;
        this.cor = cor;
        this.placa = placa;
        this.dono = dono;
    }

}
